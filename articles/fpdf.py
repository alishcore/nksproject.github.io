# Добавление страниц с контентом
add_text_page(page_1_title, page_1_content)
add_text_page(page_2_title, page_2_content)
add_text_page(page_3_title, page_3_content)

# Сохранение PDF
pdf_output_path = "Exoplanets_Educational_Pages.pdf"
pdf.output(pdf_output_path)

print(f"PDF сохранен как {pdf_output_path}")
